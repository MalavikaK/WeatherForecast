package com.example.hw03;

public class ForecastDet {

    String date;
    String temp;
    String dayIcon;
    String dayIconPhrase;
    String nightIcon;
    String nightIconPhrase;
    String mobileLink;
    String key;

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getTemp() {
        return temp;
    }

    public void setTemp(String temp) {
        this.temp = temp;
    }

    public String getDayIcon() {
        return dayIcon;
    }

    public void setDayIcon(String dayIcon) {
        this.dayIcon = dayIcon;
    }

    public String getDayIconPhrase() {
        return dayIconPhrase;
    }

    public void setDayIconPhrase(String dayIconPhrase) {
        this.dayIconPhrase = dayIconPhrase;
    }

    public String getNightIcon() {
        return nightIcon;
    }

    public void setNightIcon(String nightIcon) {
        this.nightIcon = nightIcon;
    }

    public String getNightIconPhrase() {
        return nightIconPhrase;
    }

    public void setNightIconPhrase(String nightIconPhrase) {
        this.nightIconPhrase = nightIconPhrase;
    }

    public String getMobileLink() {
        return mobileLink;
    }

    public void setMobileLink(String mobileLink) {
        this.mobileLink = mobileLink;
    }
}
